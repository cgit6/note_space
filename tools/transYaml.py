# ---- 解析每行 list ----
symbols_lists = []
for line in input_text.strip().splitlines():
    line = line.strip()
    if not line:
        continue
    symbols_lists.append(ast.literal_eval(line))  # 安全解析成 Python list

# ---- 輸出 YAML-like 格式 ----
for i, symbols in enumerate(symbols_lists):
    weights = [1] * len(symbols)

    symbols_str = ",".join(map(str, symbols))
    weights_str = ", ".join(map(str, weights))

    print(f"                - # Reel[{i}]")
    print(f"                  symbols : [{symbols_str}]")
    print(f"                  weights : [{weights_str}]")
